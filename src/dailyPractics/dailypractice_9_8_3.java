package dailyPractics;
/*
打印26个大小写字母
 */
public class dailypractice_9_8_3 {
    public static void main(String[] args) {
        char c1 = 'A';
        char c2 = 'a';
        for (int i = 0;i<26;i++){
            System.out.println(c1);
            System.out.println(c2);
            c1++;
            c2++;


        }
    }
}
